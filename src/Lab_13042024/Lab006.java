package Lab_13042024;

/* %d -> any integer - byte, short, int, long
%s -> String
%f -> float
%c -> char
 */
public class Lab006 {
    public static void main(String[] args) {

        int age = 33;
        System.out.printf("my age is %d\n", age);

        String meee = "Goodgirl";
        System.out.printf("Im a %s\n", meee);


        //final is keyword to display the value without any change further.

        final int ttage = 55;
        System.out.println(ttage);
        final short hhh = 2222;
        System.out.println(hhh);
        final String box = "whichone";
        System.out.println(box);

        int hexa = 99;

    }
}
