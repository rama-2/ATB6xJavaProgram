package Lab_18042024;

public class Binary_Operator {
    public static void main(String[] args) {
        int a= 10;
        int b= 40;
        String name = "Rama";
        System.out.println(a+name);
        System.out.println(name+a);
        System.out.println(a+b+name);
        System.out.println(name+a+b);
    }
}
