package Lab_18042024;

public class Compound_Operators {
    public static void main(String[] args) {
        int a = 20;
        int b = 40;
        b = b+30;
        b += 30;
        // +=, -=, *=, /=,%=  both statements are same.
        System.out.println(b);

        a-= 10;
        b*=9;
        System.out.println(a);
        System.out.println(b);

    }
}
